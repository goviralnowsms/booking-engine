                  <h1>Where do I start?</h1>
                  <p>Planning a trip to Africa takes time. First think about what you want to see or do most and find out the best time to experience it. That process will highlight when it's best for you to travel. Next, consider your budget and what style of travel suits you best (options are outlined below). In your planning you will also need an appreciation for distances as Africa is the world's second largest continent and travelling around it takes time.

Call us to discuss your 'wish list' and we will advise you on when best to travel and what options you should consider. We are the experts when it comes to travel times, distances, cost guides and what to expect on safari. We understand that planning a trip to Africa may seem daunting but with our extensive knowledge of the region we will prepare the perfect itinerary to suit your wishes. We’ll plan a trip that has you travelling at a reasonable pace without spending all your time in a vehicle or at airports. We’ll make sure that you get the most from your trip by considering your wish list, budget, requirements and restrictions. </p>

					<h1>What style of travel should I choose?</h1>
					<h2>Tailor-made trips </h2>
                  <p>Tailor-made tours are personalised and offer great flexibility. As opposed to group travel, private tours allow you to travel alone or with your own party. Set itineraries are available which you can customise to suit your own individual interests and needs, or we can compile an itinerary for you from start to finish. Tours are escorted by an English speaking driver-guide in a private vehicle. Tailor-made tours allow for your choices of interest, accommodation and modes of transport and you can decide how long you stay at each destination. Because you have your own driver and vehicle you can be more spontaneous and stop at the local village or craft market en route if time permits. </p>

				<h1>Scheduled group tours</h1>
                  <p>These tours depart on set dates, they have pre-planned itineraries and involve you travelling with a group of people. Scheduled tours are great value as tour operators can spread the cost of running the tour over a group of people. Accommodation can range from five star lodges and basic cabins, to camping, and tours can range from two to 73 days duration. This style of touring allows you to stretch your budget further but you are restricted to a pre-planned itinerary which offers little flexibility. </p>

				  <h1>Overland expeditions</h1>
                  <p>Overland tours are about spending your days experiencing the sights and delights of Africa, and your evenings relaxing around the campfire with fellow travellers. Travelling with a group means you can share your African experience with others and it gives you the opportunity to share the cost of extra activities which you may not have been able to afford on your own. This style of travel is ideal for travellers who want to cover great distances over long periods of time with a limited budget. If you are a solo traveller toverlanding allows you to stretch your budget further by sharing a room, or tent, and a vehicle.</p>
				   <h1>Backpacking</h1>
                  <p>It is possible to backpack in Africa but you need time on your side, a lot of patience and a great sense of humour to enjoy it. Many backpackers in Africa start off with a short overlanding or small group scheduled tour to get a feel for the region. This applies especially in east Africa where you need a way to travel around secluded game parks and obtaining gorilla permits, for example, are easier through an organised channel.</p>

				   <h1>Self-drive</h1>
                  <p>Self-drive holidays are popular in South Africa and Namibia with numerous car hire depots and hotels available. Both countries have sealed roads and highways, road signs are in English and vehicles travel on the left-hand side of the road making driving easy and enjoyable. You can simply hire a car and go with the flow, or choose to pre-book your accommodation and use a map or GPS to locate your hotel. Always consider school holiday periods if self-driving as the locals in these countries are big domestic travellers.

Road surfaces in Africa vary from excellent sealed highways to eroded dirt tracks which include river crossings. Be aware that web-based directions may not be reliable and that border crossings which appear on maps may be closed and roads may be blocked during the rainy season. African border guards love paperwork so your patience will be required if you’re driving between countries.</p>
				  <h6>Scheduled flights</h6>
				  <p></p>
				   <h6>Credit card payments</h6>
				   <p>Most countries in Africa have a national airline and occasionally a second airline in wealthier  countries. Because most Africans can’t afford to go on many holidays there isn’t necessarily a great demand for flights so you might find that flights can be irregular.</p>

				    <h6>Charter flights</h6>
				   <p>Charter flights service private lodges in some areas of the Kruger National Park in South Africa and island lodges in the Okavango Delta in Botswana. Small aircraft access these properties by landing at the lodge's airstrip or at a local airport which they operate. We can advise you on the various options available to get to your accommodation so that your holiday time is maximized fully and you don't spend unnecessary hours sitting at airports or on long, uncomfortable drives.</p>
