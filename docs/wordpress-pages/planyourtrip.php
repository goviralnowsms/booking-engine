					     <h2>East Africa</h2>
						 <h4>Kenya</h4>
						<p>Offering a combination of great game viewing and coastal resorts Kenya is popular all year round.
						<strong>The coast</strong> - The Europeans leave their cold winter and migrate south to its warmer climate making it traditionally busier on the coast between Dec – Mar but don’t let that put you off.
						<strong>Game viewing</strong> - The ultimate game viewing experience happens here between July and September when around 2.5 million animals head north to the Maasai Mara seeking its green pastures during the great migration. Book early to get your spot especially if you want to see this event from the comfort of your balcony at one of the privileged game lodges situated at the Mara River bend.
						<strong>Rains versus dry</strong> – The main rains can make access to some lodges difficult in April / May with the gentle rains of November / December creating a greener landscape making areas like the Mountains of the Moon even more picturesque.
						</p>
