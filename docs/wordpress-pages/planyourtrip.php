						<strong>The Gorillas and other primates</strong> – They aren’t called Mountains Gorillas for nothing. Living around 2000 metres and upwards the Gorillas inhabit forested regions that are covered in mist and attract plenty of rainfall. Avoiding the rainy season isn’t essential as visits can be made year round.
