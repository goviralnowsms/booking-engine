					  <div class="landscape"><h1>Tanzania</h1>
					  <p>Along with Kenya Tanzania is East Africa’s game viewing capital. Its vast plains lead into its highlands which can be cool at night but the majority of its days are warm and sunny.
					  <strong>Game viewing </strong> –  In May the herds of Zebra and Wildebeest that make up the migration start to move westward from  its eastern plains and enter Kenya making May – July the opportune time and through to September a good time to view this spectacle.  Don’t expect to be alone but do expect to be amazed.
					   <strong>Rains versus dry </strong>– The main rainy seasons are similar to Kenya with the shorter grasses of the dry season from June to October making animals easier to view. Especially the big cats for which parks in Tanzania are famous for.</p>
